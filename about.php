			<h1>About Europ.in</h1>

			<p><b><a href="http://europ.in" title="Europ.in - discover and explore Europeana content">Europ.in</a></b> is a service for visual discovery and exploration of Europeana content.</p>

			<p>It starts with a simple search screen:</p>
			<ul>
				<li>Enter search keywords and watch as images from Europeana appear before your eyes in a continuous stream.</li>
				<li>The item view shows detailed information about the image along with social sharing buttons and a link to this item on Europeana.</li>
				<li>Share the image with friends using "Pin It" and "Facebook Like" buttons.</li>
			</ul>

			<p>Explore by following these sample queries:</p>
			<ul>
				<li><a href="http://europ.in/search?q=da%20Vinci">Search for: da Vinci</a></li>
				<li><a href="http://europ.in/search?q=flowers">Search for: flowers</a></li>
				<li><a href="http://europ.in/search?q=R%C4%ABga">Search for: Rīga</a></li>
			</ul>

